package chap02.sec02.exam05;
//String 타입

public class StringExample {

	public static void main(String[] args) {

		String name = "홍길동"; // String 타입 변수 name에 문자 리터럴 "홍길동" 저장
		String job = "프로그래머"; // String 타입 변수 job에 문자 리터럴 "프로그래머" 저장
		System.out.println(name); // String 타입 변수 name에 저장된 값 출력
		System.out.println(job); // String 타입 변수 job에 저장된 값 출력
	}
}