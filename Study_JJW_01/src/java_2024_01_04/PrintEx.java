package java_2024_01_04;

public class PrintEx {

	public static void main(String[] args) {

		System.out.printf("%10d", 3334);
		System.out.print("\n");
		System.out.printf("%-10d", 3334);
		System.out.print("\n");
		System.out.printf("%010d", 3334);
		System.out.print("\n");

		System.out.printf("%010.2f", 3.141592);
	}

}
